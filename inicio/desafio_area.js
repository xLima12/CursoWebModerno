// Valor da área

//Sem usar as operações matemáticas do JavaScript
let raio = 5;
const PI = 3.14;
let area = PI * raio * raio;
console.log('O valor da área é: ' + area + 'm²');

//Com as operações matemáticas do JavaScript
area = Math.PI * Math.pow(5, 2);
console.log('O valor da área é: ' + area + 'm²')